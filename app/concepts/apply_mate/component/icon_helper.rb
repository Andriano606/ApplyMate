  def icon(name, size: 5, **html_options)
    entry = ICONS[name.to_sym]
    return ''.html_safe unless entry

    css_class = "#{ICON_SIZES[size] || "w-#{size} h-#{size}"} inline-block"
    css_class = "#{css_class} #{html_options.delete(:class)}" if html_options[:class]

    svg_attrs = { class: css_class, xmlns: 'http://www.w3.org/2000/svg', viewBox: entry[:view_box] }

    if entry[:style] == :multicolor
      svg_attrs[:fill] = 'none'
      svg_attrs.merge!(html_options)
      paths_html = entry[:paths].map { |p| helpers.content_tag(:path, '', d: p[:d], fill: p[:fill]) }.join.html_safe
      return helpers.content_tag(:svg, paths_html, **svg_attrs)
    end

    if entry[:style] == :fill
      svg_attrs[:fill] = 'currentColor'
      path_attrs = { d: entry[:path], 'fill-rule': 'evenodd', 'clip-rule': 'evenodd' }
    else
      svg_attrs[:fill] = 'none'
      svg_attrs[:stroke] = 'currentColor'
      svg_attrs[:'stroke-width'] = '1.5'
      path_attrs = { d: entry[:path], 'stroke-linecap': 'round', 'stroke-linejoin': 'round' }
    end

    svg_attrs.merge!(html_options)

    helpers.content_tag(:svg, helpers.content_tag(:path, '', **path_attrs), **svg_attrs)
  end
